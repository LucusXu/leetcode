func quickSort(A []int, start, end int) {
    var i, j int = start, start
    var p int = end - 1
    for ; i < p && j < p && i <= j; {
        if A[j] > A[p] {
            j++
        } else {
            if i != j {
                A[i], A[j] = A[j], A[i]
            }
            i++
            j++
        }
    }
    if i != p {
        A[i], A[p] = A[p], A[i]
        p = i
    }
    if p > 0 {
        quickSort(A, 0, p)
    }
    if p < end - 1 {
        quickSort(A, p + 1, end)
    }
    return
}

